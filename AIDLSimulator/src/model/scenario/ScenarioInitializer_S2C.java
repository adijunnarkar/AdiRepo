package model.scenario;
import model.participant.*;
import java.util.*;

public class ScenarioInitializer_S2C {
	
	private int numOfProducts;
	private double initialAccountBalance;
	private ArrayList<Asset> initialAssetList = new ArrayList<Asset>(); 
	private ArrayList<Product> initialProductList = new ArrayList<Product>();
	private int timeWindowForQuotes;
	private int scenarioDuration;
	private int numOfAutomatedProducts;

	
	public ScenarioInitializer_S2C(){
		
	}
	
	public void assignNumProducts (int _numProducts){
		numOfProducts = _numProducts;
	}
	
	public void assignInitialAcctBal(double initAcctBalance){
		initialAccountBalance = initAcctBalance;
	}
	
	public void assignInitialAssetList (ArrayList<Asset> _initialAssetList){
		initialAssetList = _initialAssetList;
	}
	
	public void assignInitialProductList (ArrayList<Product> _initialProductList){
		initialProductList = _initialProductList;
	}
	
	public void assignTimeWindowForQuotes (int _timeWindowQuotes){
		timeWindowForQuotes = _timeWindowQuotes;
	}
	
	public void assignScenarioDuration( int _scenarioDuration){
		scenarioDuration = _scenarioDuration;
	}
	
	public void assignNumOfAutomatedProducts( int _numOfAutomatedProducts){
		numOfAutomatedProducts = _numOfAutomatedProducts;
	}
	
	
	public int getNumofProducts(){
		return numOfProducts;
	}
	
	public double getInitialAccBalance(){
		return initialAccountBalance;
	}
	
	public ArrayList<Asset> getInitialAssetList(){
		return initialAssetList;
	}
	
	public ArrayList<Product> getinitialProductList(){
		return initialProductList;
	}
	
	public int getTimeWindowForQuotes(){
		return timeWindowForQuotes;
	}
	

}//ScenarioInitializer
